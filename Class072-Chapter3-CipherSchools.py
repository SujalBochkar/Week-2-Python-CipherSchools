# while
i = 0
while i < 10:
    print(i)
    i += 1

# for Loop
for i in range(1,11,2):
    print(i)

# break keyword
for i in range (1,11):
    if i ==5:
        break
    print(i)

# continue keyword
for i in range (1,11):
    if i ==5:
        continue
    print(i)
